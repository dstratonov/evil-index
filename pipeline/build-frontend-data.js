#!/usr/bin/env node

/**
 * Merges pipeline results.json + company-meta.json → src/data/companies.js
 *
 * Usage:
 *   node build-frontend-data.js
 *   node build-frontend-data.js --results custom-results.json
 */

import { readFile, writeFile } from 'node:fs/promises';
import { parseArgs } from 'node:util';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));

const { values: args } = parseArgs({
  options: {
    results: { type: 'string', default: 'results.json' },
    meta: { type: 'string', default: 'company-meta.json' },
    out: { type: 'string', default: join(__dirname, '..', 'src', 'data', 'companies.js') },
  },
});

async function main() {
  const results = JSON.parse(await readFile(args.results, 'utf-8'));
  const meta = JSON.parse(await readFile(args.meta, 'utf-8'));

  const companies = results
    .filter((r) => !r.error)
    .map((r, i) => {
      const m = meta[r.name] || {};
      return {
        id: i + 1,
        name: r.name,
        ticker: m.ticker || r.name.slice(0, 4).toUpperCase(),
        industry: r.industry || 'Technology',
        logo: m.logo || r.name.slice(0, 2).toUpperCase(),
        employeeCount: m.employeeCount || 'Unknown',
        founded: m.founded || null,
        hq: m.hq || 'Unknown',
        evilScore: r.evilScore,
        confidence: r.confidence,
        breakdown: r.breakdown,
        justifications: r.justifications,
        criterionConfidence: r.criterionConfidence,
        intelSummary: r.intelSummary,
        sources: r.sources,
        signals: r.signals,
        lastUpdated: r.lastUpdated,
        tags: r.tags || [],
        trending: r.trending || 'stable',
        trendingReason: r.trendingReason || '',
      };
    });

  // Sort by evil score descending by default
  companies.sort((a, b) => b.evilScore - a.evilScore);

  const js = `// Auto-generated by pipeline/build-frontend-data.js — do not edit manually
// Last generated: ${new Date().toISOString()}
// Source: pipeline/results.json + pipeline/company-meta.json

export const criteriaWeights = {
  stability: 0.2,
  compensation: 0.2,
  hiringProcess: 0.1,
  workAtmosphere: 0.3,
  careerGrowth: 0.2,
};

export const breakdownLabels = {
  stability: 'Stability',
  compensation: 'Compensation',
  hiringProcess: 'Hiring Process',
  workAtmosphere: 'Work Atmosphere',
  careerGrowth: 'Career Growth',
};

export const calculateEvilScore = (breakdown) => {
  return Math.round(
    Object.entries(criteriaWeights).reduce((sum, [key, weight]) => sum + (breakdown[key] || 0) * weight, 0),
  );
};

export const getVerdict = (score) => {
  if (score >= 80) return 'CRITICAL';
  if (score >= 60) return 'HIGH';
  if (score >= 40) return 'ELEVATED';
  return 'LOW';
};

export const getVerdictColor = (score) => {
  if (score >= 80) return '#ff1744';
  if (score >= 60) return '#ff6d00';
  if (score >= 40) return '#ffc400';
  return '#00e676';
};

export const getScoreColor = (score) => {
  const hue = 140 * (1 - score / 100);
  return \`hsl(\${hue}, 100%, 50%)\`;
};

export const companies = ${JSON.stringify(companies, null, 2)};
`;

  await writeFile(args.out, js);
  console.log(`Generated ${args.out} with ${companies.length} companies`);

  // Summary
  for (const c of companies) {
    const bar = '\u2588'.repeat(Math.round(c.evilScore / 5)) + '\u2591'.repeat(20 - Math.round(c.evilScore / 5));
    console.log(`  ${c.name.padEnd(20)} ${bar} ${c.evilScore} (${getVerdict(c.evilScore)})`);
  }
}

function getVerdict(score) {
  if (score >= 80) return 'CRITICAL';
  if (score >= 60) return 'HIGH';
  if (score >= 40) return 'ELEVATED';
  return 'LOW';
}

main().catch((err) => {
  console.error('Error:', err);
  process.exit(1);
});
